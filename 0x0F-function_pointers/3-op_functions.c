#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - returns the sum of numbers
 * @a: number 1
 * @b: number 2
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of numbers
 * @a: number 1
 * @b: number 2
 * Return: 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of numbers
 * @a: number 1
 * @b: number 2
 * Return: 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of the division of numbers
 * @a: number 1
 * @b: number 2
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of numbers
 * @a: numbre 1
 * @b: number 2
 * Return: 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
